# Python math module notes

# math module provides mathematical constants and functions
# We usually import it like this:
import math as mt


# Mathematical Constants

# mt.pi → value of π (pi)
print(mt.pi)        # 3.141592653589793

# mt.e → value of Euler's number
print(mt.e)         # 2.718281828459045


# Square root

x = 9

# mt.sqrt(x) → returns square root of x
print(mt.sqrt(x))   # 3.0


# Ceiling & Floor

x = 9.9

# mt.ceil(x)
# Returns the smallest integer >= x
print(mt.ceil(x))   # 10

# mt.floor(x)
# Returns the largest integer <= x
print(mt.floor(x))  # 9



# Summary 

# mt.pi     → π value
# mt.e      → Euler’s number
# mt.sqrt() → square root
# mt.ceil() → rounds UP
# mt.floor()→ rounds DOWN
